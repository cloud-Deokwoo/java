package day02.cal;

public class ComparisonExample {

	public static void main(String[] args) {
		// 비교 연산자 : <, <=, >, >=, ==, !=, instanceof
		int a = 10;
		int b = 5;
		
		System.out.println("a < b 의 결과 : "+(a < b));
		System.out.println("a <= b 의 결과 : "+(a <= b));
		System.out.println("a > b 의 결과 : "+(a > b));
		System.out.println("a >= b 의 결과 : "+(a >= b));
		System.out.println("a == b 의 결과 : "+(a == b));
		System.out.println("a != b 의 결과 : "+(a != b));

	}

}
